Q1:DISPLAY EMPID,EMPLOYEE_NAME,MONTHLY_SALARY,QUARTLY_SALARY,ANUAL_SALARY FOR EMPLOYEE ?
QUERY:-
=======
  SELECT EMPNO EMPID,ENAME AS EMPLOYEE_NAME , SAL AS MONTHLY_SALARY ,  SAL*3 QUARTERLY_SALARY ,  SAL*12 AS ANNUAL_SALARY
  FROM EMP;

  
  

