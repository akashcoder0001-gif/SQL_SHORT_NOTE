   MONTHLY_GROSS(MONTHLY_SAL+COMMISION+PF+HRA+OTHERS),CTC/ANUAL_PACKAGE(MONTHLY_GROSS*12) ?

QUERY:-
=======
    SELECT EMPNO AS EMPLOYEE_NO , ENAME AS EMPPLOYEE_NAME , SAL AS MONTHLY_SALARY , COMM AS COMMISSION , SAL*0.12 AS PF , SAL*0.10 HRA,
    SAL*0.15 AS DA , SAL+NVL(COMM,0)+SAL*0.12+SAL*0.10+SAL*0.15 AS MONTHLY_GROSS , (SAL+NVL(COMM,0)+SAL*0.12+SAL*0.10+SAL*0.15)*12 AS ANNUAL_PACKAGE
    FROM EMP;
 
 
 
 

